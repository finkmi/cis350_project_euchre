package cis350_project_euchre;

/**********************************************************************
 * This enum specifies each of the four suits that a given euchre card
 * may be.
 * 
 * @author Michael Fink, Charlie Dorn
 *********************************************************************/
public enum SUIT {
	/** The one with the three balls and it's black. */
	CLUB,
	
	/** The one that looks like a square but turned and it's red. */
	DIAMOND,
	
	/** The one that looks like a valentines card and it's red. */
	HEART,
	
	/** The one that looks like an arrow head and it's black. */
	SPADE
}
